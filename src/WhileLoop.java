public class WhileLoop {
    public static void main (String [] args)
    {
        int x = 0;
        while (x<20)
        {
            System.out.println("Value of x : " + x);
            x++;
                    // here the value of x will increment by 1 each loop until the desired result
            System.out.println("\n");
        }
    }
}
