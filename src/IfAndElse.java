public class IfAndElse {
    public static void main (String[] args) {
        int number = 1;

        if (number == 233) {
            System.out.println("The number is 233");
        } else if (number == 2) {
            System.out.println("The number is two.");
        } else if (number == 43) {
            System.out.println("The number is 43!");
        } else {
            System.out.println("Quite a lot!");
        }
    }
}
